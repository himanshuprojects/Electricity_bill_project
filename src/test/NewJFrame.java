    public float cal_cost(int unit, int phase) {
        float res = (float) 0.00;
        if (phase == 1) {
            if (unit <= 50) {
                res = (float) (unit * 1.45);
            } else if (unit <= 100) {
                res = (float) (50 * 1.45 + (unit - 50) * 2.60);
            } else if (unit <= 200) {
                res = (float) (100 * 3.30 + (unit - 100) * 4.30);
            } else if (unit > 200) {
                res = (float) (200 * 5.00 + (unit - 200) * 6.00);
            }
        } else if (phase == 2) {
            if (unit <= 50) {
                res = (float) (unit * 1.45);
            } else if (unit <= 100) {
                res = (float) (50 * 1.45 + (unit - 50) * 2.60);
            } else if (unit <= 200) {
                res = (float) (100 * 3.30 + (unit - 100) * 4.30);
            } else if (unit > 200) {
                float[] chrg = {5.00f, 7.20f, 8.50f, 9.00f, 9.50f};

                for (int i = 0; i < 4; i++)
                {
                    if (unit > 0) {
                        res += (float) (100 * chrg[i]);
                        unit -= 100;
                    } 
                    else { System.out.println("br");
                        break;
                    }
                }
                if (unit > 0) {
                    res += (float) (unit * chrg[4]);
                }

            }

        }
        return res;
    }

