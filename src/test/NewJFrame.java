    void updat(int id, String renam, int rename_conn, int rename_prev, int rename_pres)
    { 
        opendatabase();
    
        int ret_prev_mr,ret_prs_mr,ret_connection,ret_unit;
    
         try {
            ResultSet rs = stmt.executeQuery("select * from billnow where id=" + id);
            rs.next();
          //ret=retrieve the previous data on given id
            ret_connection= rs.getInt(3);
            ret_prev_mr = rs.getInt(4);
            ret_prs_mr=rs.getInt(5);
            ret_unit=rs.getInt(6);
            //System.out.println(""+ret_name+ret_connection+ret_prev_mr+ret_prs_mr);
          
            stmt = conn.createStatement();
           String sql;

//String query = " insert into billnow (Id, name, phase, prev, pres,unit,cost)"
      
  if(!renam.equals(""))
       {   
       sql = "UPDATE billnow SET name ='"+renam+"' WHERE id ="+ id;
      stmt.executeUpdate(sql);
        System.out.println(" Name updated ");
        }
          
  if(rename_conn!=0)
       {
         float rename_cost=cal_cost(ret_unit, rename_conn);
        
        sql = "UPDATE billnow SET phase ="+rename_conn+",cost="+rename_cost+" WHERE id ="+ id;
      stmt.executeUpdate(sql);
      System.out.println(" Connection updated");
       }
          
  if(rename_prev!=0)
       {
           int new_unit;
           if(rename_prev>=ret_prs_mr)
               new_unit=rename_prev-ret_prs_mr;
           else
               new_unit=ret_prs_mr-rename_prev;
           float rename_cost=cal_cost(new_unit, ret_connection);
       sql = "UPDATE billnow SET prev ="+rename_prev+",unit="+new_unit+",cost="+rename_cost+"WHERE id ="+ id;
      stmt.executeUpdate(sql);
      System.out.println(" Prev reading updated");
        }
             
  if(rename_pres!=0)
       {
     int new_unit;
           if(ret_prev_mr>=rename_pres)
               new_unit=ret_prev_mr-rename_pres;
           else
               new_unit=rename_pres-ret_prev_mr;
           float rename_cost=cal_cost(new_unit, ret_connection);
       sql = "UPDATE billnow SET pres ="+rename_pres+",unit="+new_unit+", cost="+rename_cost+"WHERE id ="+ id;
      stmt.executeUpdate(sql);
      System.out.println(" Present reading updated");
        }
           
        } catch (SQLException se) {
              result.setText("Cannot update.. Is this ID="+id+" is already present in database??");
            System.out.println("Cannot update.. Is this ID="+id+" is already present in database??");
            
        } catch (Exception e) {
            System.out.println("Cannot update");
            
        }finally{
             closeconn();
         }
    }
    
    
